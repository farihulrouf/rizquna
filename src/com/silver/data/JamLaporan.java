package com.silver.data;

public class JamLaporan {
  private String jam;
  
  private Double Total;
  
  private String nama_komputer;
  
  public String getJam() {
    return this.jam;
  }
  
  public void setJam(String jam) {
    this.jam = jam;
  }
  
  public Double getTotal() {
    return this.Total;
  }
  
  public void setTotal(Double total) {
    this.Total = total;
  }
  
  public String getNama_komputer() {
    return this.nama_komputer;
  }
  
  public void setNama_komputer(String nama_komputer) {
    this.nama_komputer = nama_komputer;
  }
}
